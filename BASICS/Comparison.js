/*
| Operator | Description                 | Example     | Result  |
| -------- | --------------------------- | ----------- | ------- |
| `==`     | Equal (value only)          | `5 == '5'`  | `true`  |
| `===`    | Strict equal (value + type) | `5 === '5'` | `false` |
| `!=`     | Not equal                   | `5 != 4`    | `true`  |
| `!==`    | Strict not equal            | `5 !== '5'` | `true`  |
| `>`      | Greater than                | `6 > 5`     | `true`  |
| `<`      | Less than                   | `2 < 3`     | `true`  |
| `>=`     | Greater or equal            | `5 >= 5`    | `true`  |
| `<=`     | Less or equal               | `4 <= 5`    | `true`  |
*/


