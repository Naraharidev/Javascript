/*
| Operator | Description      | Example                 |
| -------- | ---------------- | ----------------------- |
| `++`     | Increment        | `x++` or `++x`          |
| `--`     | Decrement        | `x--` or `--x`          |
| `typeof` | Type of variable | `typeof 5` → `"number"` |
| `!`      | Logical NOT      | `!true` → `false`       |
*/

let a=5;
console.log(a++)
console.log(++a)
console.log(typeof(a))
